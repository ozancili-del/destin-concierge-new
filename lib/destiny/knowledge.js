// ── HOLIDAY WINDOWS — rolls forward automatically (no annual maintenance) ────
// Replaces v1's hardcoded HOLIDAY_DATES (chat.js 654-668). Computed for the
// current year and next year every request; injected as verified context.
function nthWeekdayOfMonth(year, month, weekday, n) {
  // month 0-11, weekday 0=Sun..6=Sat, n>=1
  const first = new Date(Date.UTC(year, month, 1));
  const offset = (weekday - first.getUTCDay() + 7) % 7;
  return new Date(Date.UTC(year, month, 1 + offset + (n - 1) * 7));
}
function lastWeekdayOfMonth(year, month, weekday) {
  const last = new Date(Date.UTC(year, month + 1, 0));
  const offset = (last.getUTCDay() - weekday + 7) % 7;
  return new Date(Date.UTC(year, month + 1, 0 - offset));
}
const iso = d => d.toISOString().slice(0, 10);
const shift = (d, n) => { const x = new Date(d); x.setUTCDate(x.getUTCDate() + n); return x; };

export function computeHolidayWindows(todayIso) {
  const out = [];
  const startYear = parseInt(todayIso.slice(0, 4), 10);
  for (const year of [startYear, startYear + 1]) {
    const memorial = lastWeekdayOfMonth(year, 4, 1);           // last Mon May
    const labor    = nthWeekdayOfMonth(year, 8, 1, 1);          // first Mon Sept
    const thanks   = nthWeekdayOfMonth(year, 10, 4, 4);         // 4th Thu Nov
    const july4    = new Date(Date.UTC(year, 6, 4));
    const xmas     = new Date(Date.UTC(year, 11, 25));
    const nye      = new Date(Date.UTC(year, 11, 31));
    out.push(
      { name: "Memorial Day weekend",  arrival: iso(shift(memorial, -3)), departure: iso(memorial) },
      { name: "4th of July weekend",   arrival: iso(shift(july4, -1)),    departure: iso(shift(july4, 2)) },
      { name: "Labor Day weekend",     arrival: iso(shift(labor, -3)),    departure: iso(labor) },
      { name: "Thanksgiving weekend",  arrival: iso(shift(thanks, -1)),   departure: iso(shift(thanks, 3)) },
      { name: "Christmas",             arrival: iso(shift(xmas, -1)),     departure: iso(shift(xmas, 2)) },
      { name: "New Year's",            arrival: iso(nye),                 departure: iso(shift(nye, 2)) },
    );
  }
  // Only future windows are useful — past ones caused v1's stale-holiday problem.
  return out.filter(h => h.departure >= todayIso);
}
